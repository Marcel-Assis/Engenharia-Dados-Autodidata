# Funções matemáticas built-in:
# Exemplo de uso:
import math


print(math.sqrt(16))  # 4.0
print(math.factorial(5))  # 120
print(math.pow(2, 3))  # 8.0

# Funções trigonométricas:
print(math.sin(math.pi / 2))  # 1.0
print(math.cos(math.pi))  # -1.0
print(math.tan(math.pi / 4))  # 1.0

# Funções logarítmicas:
print(math.log(10))  # Logaritmo natural (base e) de 10
print(math.log10(100))  # Logaritmo base 10 de 100

# Constantes matemáticas:
print(math.pi)  # 3.141592653589793
print(math.e)  # 2.718281828459045

# Funções de arredondamento:
print(math.ceil(4.2))  # 5
print(math.floor(4.8))  # 4
print(round(4.5))  # 4
print(round(4.6))  # 5

# Funções de valor absoluto:
print(abs(-5))  # 5

# Funções de comparação:
print(max(1, 2, 3))  # 3
print(min(1, 2, 3))  # 1

# Funções de soma e produto:
print(sum([1, 2, 3, 4, 5]))  # 15
print(math.prod([1, 2, 3, 4, 5]))  # 120
