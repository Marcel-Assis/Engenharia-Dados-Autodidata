'''
Este é um módulo da atividade 3 chamado cal_python, com duas funções chamadas subtrai e soma
'''

def subtrai(num1, num2):
    '''Essa função subtrai dois números recebidos como parametro'''
    return num1 - num2

def soma(num1, num2):
    '''Essa função soma dois números recebidos como parametro'''
    return num1 + num2