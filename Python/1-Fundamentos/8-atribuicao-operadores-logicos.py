a_or_b = a or b
not_a = not a
num = 11
boolean = num > 0 and num < 20
is_float = type(num) == float

print("a and b:", a_and_b)
print("a or b:", a_or_b)
print("not a:", not_a)
print("num > 0 and num < 20:", boolean)
print("num é float:", is_float)
