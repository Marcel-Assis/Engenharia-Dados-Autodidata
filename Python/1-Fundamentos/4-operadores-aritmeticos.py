# Operadores aritméticos:
# + adição
# - subtração
# * multiplicação
# / divisão
# % resto da divisão
# ** exponenciação
# // divisão inteira

# Exemplos de uso:
a = 10
b = 3

print("Adição:", a + b)
print("Subtração:", a - b)
print("Multiplicação:", a * b)
print("Divisão:", a / b)
print("Resto da divisão:", a % b)
print("Exponenciação:", a ** b)
print("Divisão inteira:", a // b)

# Ordem de prioridade dos operadores:
# 1. ()
# 2. **
# 3. * / // %
# 4. + -